#include <unistd.h>
#include <stdio.h>

int	str_count(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

unsigned int	ft_strlcat(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	dlen;
	unsigned int	slen;

	dlen = str_count(dest);
	slen = str_count(src);
	i = 0;
	j = dlen;
	if (size == 0 || size <= dlen)
		return (slen + size);
	while (src[i] != '\0' && i < size - dlen -1)
	{
		dest[j] = src[i];
		i++;
		j++;
	}
	dest[j] = '\0';
	return (dlen + slen);
}
//strlcat returns length of s+d while strncat returns str
int main(void)
{
	char buffer[20] = "Hello, ";
	char *to_add = "world!";
	unsigned int size = sizeof(buffer);

	unsigned int result = ft_strlcat(buffer, to_add, size);

	printf("Buffer after ft_strlcat: '%s'\n", buffer);
	printf("Returned length: %u\n", result);

	return 0;
}